# Em Python, os operadores de comparação são usados para comparar dois
# valores. Eles retornam um valor booleano (True ou False).
# Aqui estão os principais operadores de comparação:

a = 5
b = 3
# Igual a (==): Verifica se dois valores são iguais.
print(a == b)

# Diferente de (!=): Verifica se dois valores são diferentes
print(a != b)

# Maior que (>): Verifica se o valor à esquerda é maior que o da direita
print(a > b)

# Menor que (<): Verifica se o valor à esquerda é menor que o da direita.
print(a < b)

# Maior ou igual a (>=): Verifica se o valor à esquerda é maior ou igual ao da direita.
print(a >= b)

# Menor ou igual a (<=): Verifica se o valor à esquerda é menor ou igual ao da direita.
print(a <= b)

# Esses operadores podem ser usados em
# condições em estruturas de controle, como if, while,
# e também em expressões booleanas.